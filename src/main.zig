const Beam = struct {
    size: i32 = 0,

    const width: i32 = 50;

    fn update(self: *Beam, gamepad: u8, shark_x: i32, shark_alive: *bool) void {
        //shrink over time
        self.size -= 2;
        if (self.size < 0) {
            self.size = 0;
        }
        if (gamepad & w4.BUTTON_1 != 0 and self.size == 0 and shark_alive.*) {
            self.size = width;
            const hitbox_inset = 10;
            const shark_right = (shark_x + Shark.width) - hitbox_inset;
            const shark_left = shark_x + hitbox_inset;
            const screen_middle = screen_size / 2;
            const beam_half = (width / 2);
            const beam_left = screen_middle - beam_half;
            const beam_right = screen_middle + beam_half;
            if (shark_right >= beam_left and shark_left <= beam_right) {
                shark_alive.* = false;
            }
        }
    }
    fn draw(self: *const Beam) void {
        if (self.size > 0) {
            w4.DRAW_COLORS.* = 0x0002;
            w4.rect(Surfer.x - @divFloor(self.size, 2), 0, @intCast(self.size), screen_size);
        }
    }
};
const ObstacleType = enum { square };
const Obstacle = struct {
    x: f32,
    t: ObstacleType,

    const y: i32 = 63;
    const size = 8;
    fn isColliding(self: *const Obstacle, other_x: f32, other_y: f32, other_width: f32, other_height: f32) bool {
        return (self.x <= other_x + other_width and self.x + size >= other_x) and (y <= other_y + other_height and y + size >= other_y);
    }
    fn update(self: *Obstacle, camera_offset: f32) void {
        self.x += camera_offset;
    }
    fn draw(self: *const Obstacle) void {
        w4.DRAW_COLORS.* = 0x42;
        w4.rect(@intFromFloat(self.x), y, size, size);
    }
};
const Obstacles = struct {
    list: [20]?Obstacle = @splat(null),

    fn spawnTypeAt(self: *Obstacles, t: ObstacleType, x: f32) void {
        const maybe_free_slot: ?*?Obstacle = for (&self.list) |*obs| {
            if (obs.* == null) {
                break obs;
            }
        } else null;

        if (maybe_free_slot) |obstacle| {
            w4.trace("spawned obstacle");
            obstacle.* = Obstacle{ .t = t, .x = x };
        } else {
            w4.trace("failed to spawn obstacle");
        }
    }
    fn spawnRandom(self: *Obstacles, rng: std.Random, camera_offset: f32) void {
        const max_off = 30.00;
        const obs_type = rng.enumValue(ObstacleType);
        const x = rng.float(f32) * max_off + camera_offset + screen_sizef;
        self.spawnTypeAt(obs_type, x);
    }
    fn update(self: *Obstacles, camera_offset: f32) void {
        for (0..self.list.len) |i| {
            if (self.list[i]) |*obstacle| {
                obstacle.update(camera_offset);
            }
        }
    }
    fn draw(self: *const Obstacles) void {
        for (&self.list) |*slot| {
            if (slot.*) |obstacle| {
                obstacle.draw();
            }
        }
    }
};
const PRNG = std.Random.DefaultPrng;
const State = struct {
    surfer: Surfer = .{},
    beam: Beam = .{},
    shark: Shark = .{},
    water: Water = .{},
    prev_button2_state: bool = false,
    moved: bool = false,
    prng: PRNG = .init(0),
    obstacles: Obstacles = .{},
    ticks: u64 = 0,
    distance: f32 = 0,

    fn update(self: *State) void {
        const gamepad = w4.GAMEPAD1.*;

        //reset button
        const curr_button2_state = gamepad & w4.BUTTON_2 != 0;
        if (curr_button2_state and !self.prev_button2_state and self.beam.size == 0) {
            self.shark.alive = true;
            self.surfer.speed = 0;
            self.shark.x = screen_sizef;
        }
        self.prev_button2_state = gamepad & w4.BUTTON_2 != 0;

        //update
        const camera_offset: f32 = if (self.shark.alive) (-self.surfer.speed) else 0;
        self.distance -= camera_offset;
        self.beam.update(gamepad, @intFromFloat(self.shark.x), &self.shark.alive);
        if (self.shark.alive) {
            self.surfer.update(gamepad);
            self.shark.update(camera_offset);
            self.water.update(camera_offset);
            self.obstacles.update(camera_offset);
        }
        if (!self.moved and gamepad & w4.BUTTON_RIGHT != 0)
            self.surfer.startMoving();
        if (gamepad & (w4.BUTTON_LEFT | w4.BUTTON_RIGHT | w4.BUTTON_1) != 0) {
            self.moved = true;
            state.prng.seed(self.ticks);
        }
        if (self.moved and self.ticks % (60 * 20) == 0) {
            self.surfer.speedUp();
        }
        //DEBUG
        if (gamepad & w4.BUTTON_DOWN != 0) {
            self.obstacles.spawnRandom(self.prng.random(), camera_offset);
        }
        self.ticks +%= 1;
    }
    fn drawScore(self: *const State) void {
        var buf: [64]u8 = undefined;
        const str = std.fmt.bufPrint(&buf, "{:.0}", .{self.distance / 10}) catch "SCORE TOO BIG";
        const x = (screen_size / 2) - (@as(i32, @intCast(str.len)) * 8);
        w4.DRAW_COLORS.* = 0x02;
        w4.text(str, x, 0);
    }
    fn draw(self: *const State) void {
        if (!self.moved) {
            w4.DRAW_COLORS.* = 2;
            const title: []const u8 = "My dream game :3";
            w4.text(title, (screen_size / 2) - ((title.len / 2) * w4.FONT_SIZE), 10);
        }

        self.water.draw();
        self.obstacles.draw();
        self.surfer.draw();
        self.shark.draw();
        self.beam.draw();
        self.drawScore();
    }
};
var state: State = .{};

export fn start() void {
    w4.PALETTE.* = .{
        0x5995d1, //sky blue (light)
        0xffffff, //white
        0x1d456d, //water blue (dark)
        0x58dd58, //green
    };
}

export fn update() void {
    state.update();
    state.draw();
}
