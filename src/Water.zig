pub fn update(self: *Water, camera_offset: f32) void {
    self.x += camera_offset;
    //move them forward to keep looping so we never run out of water
    while (self.x <= -width) {
        self.x += width;
    }
}
pub fn draw(self: *const Water) void {

    //only the top of the water uses a sprite because it's detailed
    w4.DRAW_COLORS.* = 0x0030;
    //we use two to make it look endless
    const int_x: i32 = @intFromFloat(self.x);
    w4.blit(&sprite, int_x, y, width, height, sprite_flags);
    w4.blit(&sprite, int_x + width, y, width, height, sprite_flags);

    w4.DRAW_COLORS.* = 0x0003;
    w4.rect(0, y + height, width, w4.screen_size - (y + height));
}
