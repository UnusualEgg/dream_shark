pub fn speedUp(self: *Surfer) void {
    self.speed += speed_change;
}
pub fn startMoving(self: *Surfer) void {
    self.speed += speed_change * 3;
}
pub fn update(self: *Surfer, gamepad: u8) void {
    self.speed = std.math.clamp(self.speed, 0, max_speed);

    if (gamepad & w4.BUTTON_UP != 0 and self.jump_time == 0 and self.speed != 0) {
        self.jump_time += 1;
    } else if (self.jump_time > 0) {
        self.jump_time += 1;
        self.y_off = (@sin((self.jump_time / jump_height) * std.math.pi) / (std.math.pi / 2.0)) * jump_height;
        if (self.y_off <= 0) {
            self.y_off = 0;
            self.jump_time = 0;
        }
    }
}
pub fn draw(self: *const Surfer) void {
    w4.DRAW_COLORS.* = 0x0040;
    //3 equally spaced states of rotation
    //f(x) = (2x)/max
    const surfer_index: usize = @intFromFloat((self.speed * 2.0) / max_speed);
    //blit uses top left corner as origin
    //so move surfer half over
    w4.blit(&sprite[surfer_index], x - (width / 2), @intFromFloat(y - self.y_off), width, height, sprite_flags);
}
